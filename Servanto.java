
public class Servanto {
	protected String nom;
	protected String classe;
	protected int rareté;
	protected int atk;
	protected int pv;
	protected int nplvl;
	protected String np;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getClasse() {
		return classe;
	}
	public void setClasse(String classe) {
		this.classe = classe;
	}
	public int getRareté() {
		return rareté;
	}
	public void setRareté(int rareté) {
		this.rareté = rareté;
	}
	public int getAtk() {
		return atk;
	}
	public void setAtk(int atk) {
		this.atk = atk;
	}
	public int getPv() {
		return pv;
	}
	public void setPv(int pv) {
		this.pv = pv;
	}
	public int getNplvl() {
		return nplvl;
	}
	public void setNplvl(int nplvl) {
		this.nplvl = nplvl;
	}
	public String getNp() {
		return np;
	}
	public void setNp(String np) {
		this.np = np;
	}
	
}
